from Carnivoro import Carnivoro


class Perro(Carnivoro):

    def cazar(self):
        print('El perro está cazando')

    def comer(self):
        print('El perro está comiendo')
    
    def dormir(self):
        print('El perro está durmiendo')

    def respirar(self):
        print('El perro puede respirar')

